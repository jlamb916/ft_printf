#include "libftprintf.h"

int		ft_atoi(char *s)
{
	int res;
	int sign;

	res = 0;
	sign = 1;
	while (*s == ' ' || *s == '\t' || *s == '\v' || *s == '\f' ||
			*s == '\f' || *s == '\n')
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
		res = res * 10 + *s++ - '0';
	return (res * sign);
}
